class DisplayTrackMap extends Component {
  render() {
    const { trkpts } = this.props
    return (
      <MapView
        style={{ flex: 1 }}
        //customMapStyle={customMapStyle}
        region={{
          latitude: trkpts[0].latitude,
          longitude: trkpts[0].longitude,
          latitudeDelta: 0.015,
          longitudeDelta: 0.0121
        }}
      >
        <MapView.Polyline
          styles={{ zIndex: 5 }}
          coordinates={trkpts}
          strokeWidth={2}
          strokeColor='red'
        />
        <MapView.Marker coordinate={trkpts[0]} />
        <MapView.Marker coordinate={_.last(trkpts)} />
      </MapView>
    )
  }
}
